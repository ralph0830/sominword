  bool _checkSimilarity(String userAnswer, String correctAnswer) {
    // Í∞ÑÎã®Ìïú Ïú†ÏÇ¨ÎèÑ Í≤ÄÏÇ¨ (Ïã§Ï†úÎ°úÎäî Îçî Ï†ïÍµêÌïú ÏïåÍ≥†Î¶¨Ï¶ò ÏÇ¨Ïö© Í∞ÄÎä•)
    if (userAnswer.length < 3 || correctAnswer.length < 3) {
      return userAnswer == correctAnswer;
    }
    
    // Î†àÎ≤§ÏäàÌÉÄÏù∏ Í±∞Î¶¨ Í∏∞Î∞ò Ïú†ÏÇ¨ÎèÑ
    int distance = _levenshteinDistance(userAnswer, correctAnswer);
    int maxLength = userAnswer.length > correctAnswer.length 
        ? userAnswer.length 
        : correctAnswer.length;
    
    double similarity = 1.0 - (distance / maxLength);
    return similarity >= 0.7; // 70% Ïù¥ÏÉÅ Ïú†ÏÇ¨ÌïòÎ©¥ Ï†ïÎãµÏúºÎ°ú Ïù∏Ï†ï
  }

  int _levenshteinDistance(String s1, String s2) {
    if (s1 == s2) return 0;
    if (s1.isEmpty) return s2.length;
    if (s2.isEmpty) return s1.length;

    List<int> v0 = List<int>.filled(s2.length + 1, 0);
    List<int> v1 = List<int>.filled(s2.length + 1, 0);

    for (int i = 0; i <= s2.length; i++) {
      v0[i] = i;
    }

    for (int i = 0; i < s1.length; i++) {
      v1[0] = i + 1;

      for (int j = 0; j < s2.length; j++) {
        int cost = (s1[i] == s2[j]) ? 0 : 1;
        v1[j + 1] = [v1[j] + 1, v0[j + 1] + 1, v0[j] + cost].reduce((a, b) => a < b ? a : b);
      }

      List<int> temp = v0;
      v0 = v1;
      v1 = temp;
    }

    return v0[s2.length];
  }

  void _nextQuestion() {
    setState(() {
      _currentIndex++;
      _showFeedback = false;
      _lastWords = '';
      _textController.clear();
    });

    if (_currentIndex >= _words.length) {
      _completeQuiz();
    } else {
      _setCurrentWord();
    }
  }

  void _completeQuiz() {
    setState(() {
      _isQuizComplete = true;
    });
  }

  void _restartQuiz() {
    setState(() {
      _currentIndex = 0;
      _score = 0;
      _totalQuestions = 0;
      _isQuizComplete = false;
      _showFeedback = false;
      _lastWords = '';
      _textController.clear();
    });
    _setCurrentWord();
  }

  Future<void> _playWord() async {
    await _flutterTts.speak(_currentWord);
  }

  void _submitAnswer() {
    if (_textController.text.trim().isNotEmpty) {
      setState(() {
        _lastWords = _textController.text.trim();
      });
      _checkAnswer();
    }
  }

